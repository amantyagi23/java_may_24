public class Loop_2D {
    public static void main(String[] args) {
        for (int i = 0; i < 6; i++) {
            for (int j = 0; j < 6; j++) {
                for (int z = 0; z < 6; z++) {
                    System.out.println(i + " " + j + " " + z);
                }
            }
        }
    }
}
